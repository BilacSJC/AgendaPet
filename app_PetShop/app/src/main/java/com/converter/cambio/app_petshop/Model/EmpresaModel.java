package com.converter.cambio.app_petshop.Model;

public class EmpresaModel {
    private int emp_id;
    private String emp_token;
    private String emp_auth_key;
    private String emp_nome;
    private long emp_telefone;
    private long emp_cnpj;
    private String emp_email;
    private String emp_senha;
    private int emp_endereco;


    public int getEmp_id() {
        return emp_id;
    }

    public void setEmp_id(int emp_id) {
        this.emp_id = emp_id;
    }

    public String getEmp_token() {
        return emp_token;
    }

    public void setEmp_token(String emp_token) {
        this.emp_token = emp_token;
    }

    public String getEmp_auth_key() {
        return emp_auth_key;
    }

    public void setEmp_auth_key(String emp_auth_key) {
        this.emp_auth_key = emp_auth_key;
    }

    public String getEmp_nome() {
        return emp_nome;
    }

    public void setEmp_nome(String emp_nome) {
        this.emp_nome = emp_nome;
    }

    public long getEmp_telefone() {
        return emp_telefone;
    }

    public void setEmp_telefone(long emp_telefone) {
        this.emp_telefone = emp_telefone;
    }

    public long getEmp_cnpj() {
        return emp_cnpj;
    }

    public void setEmp_cnpj(long emp_cnpj) {
        this.emp_cnpj = emp_cnpj;
    }

    public String getEmp_email() {
        return emp_email;
    }

    public void setEmp_email(String emp_email) {
        this.emp_email = emp_email;
    }

    public String getEmp_senha() {
        return emp_senha;
    }

    public void setEmp_senha(String emp_senha) {
        this.emp_senha = emp_senha;
    }

    public int getEmp_endereco() {
        return emp_endereco;
    }

    public void setEmp_endereco(int emp_endereco) {
        this.emp_endereco = emp_endereco;
    }
}
